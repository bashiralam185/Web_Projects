                <h1>Things to remember while writing cover letter</h1>
            <br><br>
            <h2>1. Don't just rehash your resume</h2>
            <p>What's the first thing to know about how to write a cover letter? Your words should do more than restate salient details from your resume. Check out this brief checklist of important functions of a cover letter:

    Draw attention to specific skills and experience that make you an ideal candidate.
    Mention other relevant skills your resume may not illustrate.
    Explain why you would love to have the job in question.
    Show you've done research on the company, its mission and key leadership</p>

            <h2>2. Tailor your cover letter to a specific job</h2>
            <p>Don’t use a one-size-fits-all cover letter template for all the positions you apply for. If you do, you’re missing the point: Only a letter that’s targeted to the job at hand will make a positive impression. Write a cover letter employers can't ignore by tying it to the elements of the job that match your unique skills and experience. What are they asking for that you’re especially good at? Those are the points to stress when writing a cover letter.

    Just as important, gather facts and figures that support your claims. For example, if you're applying for a managerial role, mention the size of teams and budgets you’ve managed. If it’s a sales role, describe specific sales goals you've achieved.</p>
            <h2>3. Be proud of your past accomplishments</h2>
            <p>Companies want confident employees who love their work. They know these are the people who tend to perform better, serve as stronger team members and have greater potential to grow along with the business. Don’t hesitate to brag a little about your most pertinent achievements.</p>
            <h2>4. Keep it brief</h2>
            <p>The barrage of information coming at all of us today has created attention spans that are shorter than ever before. Cover letters are no exception. Managers are often inundated with applications, so economy of words matters. Keep your cover letter to no more than one page if printed. Short is sweet.</p>
            <h2> 5. Address the hiring manager personally</h2>
            <p>Just as you personalize your resume to the role, you should also address the cover letter to the person actually hiring for the position. If it’s not spelled out in the job posting, call the employer's main phone number and ask for the name and title of the hiring manager. If you’re still in school or just out, your career services office may be able to help you identify the right contact at a company.</p>
