                <h1>Steps to build perfect Resume</h1>
            <br><br>
            <h2>1. Contact information</h2>
            <p>While this section may seem obvious, there are a few factors to consider. For instance, how will you display your name? Are you planning to use a nickname, such as Bob, or will you use your full name for the job search? Whichever you choose, make sure you consistently represent your name on all your personal branding materials such as your business cards, LinkedIn profile, and online portfolio or blog.

                Select one email address and one phone number to include on your resume. I recommend setting up an email address that's dedicated to your job-search activities and using your cell phone number on your resume, as this gives you the ability to control the voicemail message, who answers the phone, and when.</p>

            <h2>2.Online presence</h2>
            <p>In addition to your LinkedIn account, the perfect resume should include any links that are relevant to your work, such as a personal website, portfolio, or blog. If your work involves social media, you may include the links to other social media accounts such as Twitter, Instagram, and so forth.</p>
            <h2>3. Sample job descriptions</h2>
            <p>When you're writing the perfect resume, it should be tailored to support a specific job goal. One of the best ways to ensure your resume is properly positioned is to identify sample job descriptions that you're interested in and qualified to perform.

                Search online and gather a few job postings that represent the type of position you're targeting. It doesn't matter if the location is ideal; for this purpose, you should only be concerned with the job description and its requirements.
                
                Copy and paste the text of the description itself into a Word or Google document and then highlight or bold any requirements or desirable skills from the posting you possess. This will help you or your writer identify which of your qualifications should be showcased throughout the resume.</p>
            <h2>4. Technical skills and proficiencies</h2>
            <p>What technical platforms and tools are you proficient? List all that apply to your work. Be specific and as comprehensive as possible. This list can include anything from social media platforms to project management systems and computer languages. If you've worked with proprietary platforms, list those as well.

                Need to brush up on a skill or tool that's routinely popping up in the job descriptions you're targeting? Check out edX, Coursera, and SkillShare for free or low-cost online courses.</p>
            <h2> 5. Your professional experience</h2>
            <p>Start with your most recent job and work your way backward. The perfect resume should detail out all your professional positions within the past 15 years. If you served in the military or held a board position, list this experience as you would any other role in your work history. If you recently graduated from college, include your internships and any work experience that took place since you entered college.

                For each role, list the following information:
                
                Company Name and URL
                Job Title: If your title is very specific to your organization, you can include a translation of sorts in parentheses next to your official job title.
                Start and End Dates: Include the month and year for each of these dates.
                Job Description: Think about your roles and responsibilities as they relate to your target role. This is especially important if you'd like to change careers. Include details such as how many people you managed or supervised, the territories you covered, etc.</p>
